#include "libft.h"

void	ft_putnbr(int n)
{
	char nbr;

	nbr = 0;
	if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
	}
	if (n == -2147483648)
	{
		write(1, "2", 1);
		ft_putnbr(147483648);
		return ;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	nbr = n % 10 + '0';
	write(1, &nbr, 1);
}

#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	res;

	i = 0;
	j = 0;
	while (str[i])
		i++;
	i--;
	while (j < i)
	{
		res = str[j];
		str[j] = str[i];
		str[i] = res;
		j++;
		i--;
	}
	return (str);
}

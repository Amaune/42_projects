#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char nbr;

	nbr = 0;
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n == -2147483648)
	{
		write(fd, "2", 1);
		ft_putnbr_fd(147483648, fd);
		return ;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	nbr = n % 10 + '0';
	write(fd, &nbr, 1);
}
